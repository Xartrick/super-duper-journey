from AnimationBase import AnimationBase

from sage.all import matrix, sin, cos, pi

class AnimationFractal(AnimationBase):
	def render(self, frameNumber, filename):
		self.frameManager.setCurrentFrame(frameNumber)

		look_at = (0.5, 0, 0.5)

		alpha = (2.0 * pi) * (float(frameNumber / 4.0) / float(self.getDuration() / 4.0))
		P     = matrix(2, 1, [1.0, 1.0])
		R     = matrix(2, 2, [cos(alpha), -sin(alpha), sin(alpha), cos(alpha)])

		pos = R * P

		camera_center = (float(pos[0][0]), 0.75, float(pos[1][0]))

		self.initializeWindow(camera_center, look_at)

		self.window.light((1, 2, -3), 0.2, (1, 0.5, 1))

		self.window.texture('blue', ambient=0.2, diffuse=1.0, specular=0.0, opacity=1.0, color=(0,0,1))

		fractalSize = self.getFractalSize()
		fractal     = self.getFractal()

		cube_scale = 1. / float(fractalSize)

		for y, yFractal in enumerate(fractal):
			for x, xyFractal in enumerate(yFractal):
				for z, xyzFractal in enumerate(xyFractal):
					if xyzFractal == 1:
						self.drawCube(cube_scale * x, cube_scale * y, cube_scale * z, cube_scale, 'blue')

		self.window.save(filename)

